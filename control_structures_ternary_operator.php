<?php
/*

If you only want to do a simple if-else test (without any elseif
blocks), you can use the ternary operator ( ? ). The syntax is as
follows:

    Condition ? Task A : Task B

*/
$a = (7 == 7 ? 'Yes' : 'No'); // 7 == 7 returns true -> 'yes' 
echo $a; 

echo "<br>";

